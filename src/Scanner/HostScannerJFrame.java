    private String IPAddOne(String ip) {
        String sub1 = ip.substring(0,ip.indexOf("."));
        String sub = ip.substring(ip.indexOf(".")+1);
        
        String sub2 = sub.substring(0,ip.indexOf("."));
        sub = sub.substring(ip.indexOf(".")+1);
        
        String sub3 = sub.substring(0,ip.indexOf("."));
        String sub4 = sub.substring(ip.indexOf(".")+1);
        int n = Integer.parseInt(sub4)+1;
        
        return sub1+"."+sub2+"."+sub3+"."+n;
    }
