    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
           // 扫描按钮动作;
           String start = jTextField1.getText();
           String end = jTextField2.getText();
           String host = start;
           
           InetAddress addr;
           int timeOut = 1000;
           boolean status = false;
           end = IPAddOne(end);
           while(!host.equals(end)){
               try{
                   addr = InetAddress.getByName(host);
                   status = addr.isReachable(timeOut);
               }catch(IOException ex) { }
               
               if(status){
                   jTextArea1.append(host+"是活跃的.\n");
                   jTextArea1.paintImmediately(jTextArea1.getBounds());
               }else{
                   jTextArea1.append(host+"离线或不存在.\n");
                   jTextArea1.paintImmediately(jTextArea1.getBounds());
               }
               host = IPAddOne(host);
           }
           
    }//GEN-LAST:event_jButton1ActionPerformed

